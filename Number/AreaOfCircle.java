public class AreaOfCircle {
		public static void main(String[] args) {
			double radius=3;
			double res=area(radius);
			System.out.println("The Area Of Circle is:"+res);
		}
		
		public static double area(double r) {
			return 3.142*r*r;
				
		}

}
