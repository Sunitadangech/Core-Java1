 public class AreaOfRectangle {
	public static void main(String[] args) {
		double length=4;
		double width=8;
		double res=area(length,width);
		System.out.println("The Area Of Rectangle is:"+res);
	}
	
	public static double area(double l,double w) {
		return l*w;
			
	}

}
