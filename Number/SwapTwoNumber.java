package com.Number;
//Question:17
public class SwapTwoNumber {
	public static void main(String[] args) {
		int a=10;
		int b=20;
		int temp=a;
		System.out.println("Before Swapping : "+"a="+a + " & "+"b="+b);
		temp=a;
		a=b;
		b=temp;
		System.out.println("After Swapping : "+"a="+a + " & "+"b="+b);
	}

}
